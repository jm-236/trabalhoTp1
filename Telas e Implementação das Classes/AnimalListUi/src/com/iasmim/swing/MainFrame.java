    private static void adicionarPlaceholder(JTextField textField, String placeholder) {
        // Adicionar um FocusListener para limpar e restaurar o texto padr√£o
        textField.addFocusListener(new FocusListener() {
            @Override
            public void focusGained(FocusEvent e) {
                if (textField.getText().equals(placeholder)) {
                    textField.setText(""); // Limpar o texto
                    textField.setForeground(Color.BLACK); // Alterar cor para texto real
                }
            }

            @Override
            public void focusLost(FocusEvent e) {
                if (textField.getText().isEmpty()) {
                    textField.setText(placeholder); // Restaurar o texto padr√£o
                    textField.setForeground(Color.GRAY); // Alterar cor para indicar placeholder
                }
            }
        });

        // Configurar o texto padr√£o e a cor inicial
        textField.setText(placeholder);
        textField.setForeground(Color.GRAY);
    }

