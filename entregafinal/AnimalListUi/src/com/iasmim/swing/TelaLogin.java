    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:
        String cpf = usernameField.getText();
        String senha = new String(senhaField.getPassword());

        if (cpf.length() != 11){
            JOptionPane.showMessageDialog(null, "Tamanho do CPF inv√°lido.");
            return;
        }
        Optional<Funcionario> funcionarioOptional = funcionarioDAO
        .buscarFuncionario(cpf);

        if (funcionarioOptional.isEmpty()){
            JOptionPane.showMessageDialog(null, "Nehum usu√°rio encontrado com o cpf " + cpf + ".");
            return;
        }
        Funcionario funcionario = funcionarioOptional.get();

        if(funcionario.getSenha().equals(senha)){
            //System.out.println("Senha correta! Entrando na tela principal");
            JOptionPane.showMessageDialog(null, "Login realizado com sucesso.");
            this.setVisible(false);
            TelaPrincipal main = new TelaPrincipal(funcionario);
            main.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Senha incorreta.");
        }
    }//GEN-LAST:event_loginButtonActionPerformed

